    //Variable inmutable de tipo decimal
    val total: Double
    total = 7000.24
    //variable inmutable de tipo alfanumerica
    val titulo: String
    titulo = "sistema de ventas"
    // variable de tipo mutable, las variables mutables empiezan con var
    var mes: Int
    mes = 1
